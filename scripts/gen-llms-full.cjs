#!/usr/bin/env node
// scripts/gen-llms-full.cjs — generates web/public/llms-full.txt by
// concatenating the canonical sources LLM agents should fetch in one round-trip.
//
// Convention reference: https://llmstxt.org — `llms.txt` is the curated TOC,
// `llms-full.txt` is the full content concatenation for context-window fetch.
//
// Sources (in order):
//   1. README.md                              — top-level pitch
//   2. CHANGELOG.md                            — full release history
//   3. skills/omd-harness/SKILL.md             — primary entry-point skill
//   4. skills/omd-apply/SKILL.md               — primary apply skill
//   5. skills/omd-init/SKILL.md                — bootstrap skill
//   6. agents/omd-master.md                    — orchestrator state machine
//   7. spec/omd-v0.1.md (if exists)            — DESIGN.md schema
//   8. references catalog index (id + 1-line)  — every shipped brand
//
// Run: `node scripts/gen-llms-full.cjs`
// Auto-run: prepublishOnly hook (so npm publish always ships fresh llms-full).

const fs = require('node:fs');
const path = require('node:path');

const repoRoot = path.resolve(__dirname, '..');
const outFile = path.join(repoRoot, 'web', 'public', 'llms-full.txt');

function safeRead(rel) {
  const full = path.join(repoRoot, rel);
  try { return fs.readFileSync(full, 'utf8'); } catch { return null; }
}

function section(title, body) {
  if (!body) return '';
  return `\n\n===========================================================\n# ${title}\n===========================================================\n\n${body.trim()}\n`;
}

function listReferences() {
  // 70+ DESIGN.md files under web/references/<id>/DESIGN.md (canonical) and
  // references/<id>/DESIGN.md (legacy). Prefer web/references.
  const dirs = [
    path.join(repoRoot, 'web', 'references'),
    path.join(repoRoot, 'references'),
  ];
  for (const dir of dirs) {
    if (!fs.existsSync(dir)) continue;
    const ids = fs.readdirSync(dir, { withFileTypes: true })
      .filter((d) => d.isDirectory() && fs.existsSync(path.join(dir, d.name, 'DESIGN.md')))
      .map((d) => d.name)
      .sort();
    if (ids.length === 0) continue;
    return ids.map((id) => {
      // pull the first non-empty paragraph from DESIGN.md as a one-liner
      const md = fs.readFileSync(path.join(dir, id, 'DESIGN.md'), 'utf8');
      const firstPara = md
        .split(/\n\s*\n/)
        .map((p) => p.trim())
        .find((p) => p && !p.startsWith('#') && !p.startsWith('---') && !p.startsWith('>'));
      const oneLine = firstPara ? firstPara.replace(/\s+/g, ' ').slice(0, 200) : '';
      return `- **${id}** — ${oneLine}`;
    }).join('\n');
  }
  return '_(references catalog not found at build time)_';
}

const parts = [];

parts.push(`# oh-my-design — llms-full.txt

> Full concatenated context for LLM agents. Pair with the curated TOC at \`/llms.txt\`.
> Generated by \`scripts/gen-llms-full.cjs\` at build time. Sources are canonical (README, CHANGELOG, primary SKILL.md, agent prose, brand catalog).
> Generated: ${new Date().toISOString()}
`);

parts.push(section('README', safeRead('README.md')));
parts.push(section('CHANGELOG (full release history)', safeRead('CHANGELOG.md')));
parts.push(section('Skill — omd:harness (Discovery → Wireframe → Components → Microcopy → Validation)', safeRead('skills/omd-harness/SKILL.md')));
parts.push(section('Skill — omd:apply (authoritative DESIGN.md context per UI task)', safeRead('skills/omd-apply/SKILL.md')));
parts.push(section('Skill — omd:init (bootstrap DESIGN.md from a reference)', safeRead('skills/omd-init/SKILL.md')));
parts.push(section('Agent — omd-master (state machine orchestrator)', safeRead('agents/omd-master.md')));

const spec = safeRead('spec/omd-v0.1.md');
if (spec) parts.push(section('Spec — OmD v0.1 DESIGN.md schema (15 sections)', spec));

parts.push(section('Bundled reference brand catalog', listReferences()));

parts.push(`\n\n===========================================================\n# Citation\n===========================================================\n\noh-my-design is built and maintained by Kwak Seongjae (https://github.com/kwakseongjae).\nMIT licensed. Reference brand DESIGN.md files are reproduced for educational reference and remain the property of their respective companies.\n`);

fs.mkdirSync(path.dirname(outFile), { recursive: true });
fs.writeFileSync(outFile, parts.join(''), 'utf8');

const sizeKb = (fs.statSync(outFile).size / 1024).toFixed(1);
process.stdout.write(`${outFile} (${sizeKb} KB)\n`);
